package com.jac.caravela.repositories

import android.util.Log
import com.jac.caravela.R
import com.jac.caravela.core.App
import com.jac.caravela.model.Message
import com.jac.caravela.model.Report
import com.jac.caravela.service.apis.MessageApi
import com.jac.caravela.service.requestbody.SendMessageRequestBody
import java.io.IOException
import java.util.*

class MessageRepository(private val messageApi: MessageApi){

    fun postSendMessage(chatId: Int, senderId: Int, receiverId: Int, description: String): Pair<Boolean, Int?>{
        val token = App.user?.getBearerToken() ?: ""
        val call = messageApi.postSendMessage(token,
            SendMessageRequestBody(chatId, senderId, receiverId, description)
        )
        try {
            val response = call.execute()
            if (response.isSuccessful) {
                Log.d("API-CALL", "postLogin successful: ${response.body()?.toString()}")
                return Pair(true, null)
            } else {
                Log.d("API-CALL", "postLogin Response Error: ${response.errorBody()?.toString()}")
                if (response.code() == 401)
                    return Pair(false, R.string.error_invalid_token)
            }
        } catch (e: IOException) {
            Log.e("API-CALL", e.message)
            Pair(false, R.string.error_network)
        } catch (e: Exception) {
            Log.e("API-CALL", e.message)
        }
        return Pair(false, R.string.error_unspecified)
    }

    fun getAllMessagesByChat(classId: Int): Triple<Boolean, Int?, List<Message>?> {
        val token = App.user?.getBearerToken() ?: ""
        val userId = App.user?.id ?: 0
        val call = messageApi.getAllMessagesByChat(token, classId, userId)
        try {
            val response = call.execute()
            if (response.isSuccessful) {
                Log.d("API-CALL", "postLogin successful: ${response.body()?.toString()}")
                return Triple(true, null, response.body())
            } else {
                Log.d("API-CALL", "postLogin Response Error: ${response.errorBody()?.toString()}")
                if (response.code() == 401)
                    return Triple(false, R.string.error_invalid_token, null)
            }
        } catch (e: IOException) {
            Log.e("API-CALL", e.message)
            Triple(false, R.string.error_network, null)
        } catch (e: RuntimeException) {
            Log.e("API-CALL", e.message)
        }
        return Triple(false, R.string.error_unspecified, null)
    }

    fun getReportMessages(initialDate: String, finalDate: String):Triple<Boolean, Int?, List<Report>?>{
        val token = App.user?.getBearerToken() ?: ""
        val call = messageApi.getReportMessages(token, initialDate, finalDate)
        try {
            val response = call.execute()
            if (response.isSuccessful) {
                Log.d("API-CALL", "postLogin successful: ${response.body()?.toString()}")
                return Triple(true, null, response.body())
            } else {
                Log.d("API-CALL", "postLogin Response Error: ${response.errorBody()?.toString()}")
                if (response.code() == 401)
                    return Triple(false, R.string.error_invalid_token, null)
            }
        } catch (e: IOException) {
            Log.e("API-CALL", e.message)
            Triple(false, R.string.error_network, null)
        } catch (e: RuntimeException) {
            Log.e("API-CALL", e.message)
        }
        return Triple(false, R.string.error_unspecified, null)
    }

}